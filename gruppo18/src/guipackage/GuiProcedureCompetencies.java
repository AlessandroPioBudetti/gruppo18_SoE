    private void visualizzaCompetenze() {
        String[] compet = competenze.visualizeCompetencies();
        jListCompe.setListData(compet);
    }

    private void visualizzaProcedure() {
        String[] proced = procedure.visualizeProcedure();
        jListProcedure.setListData(proced);
    }
    
    private void visualizzaCompetenzeProcedure(String selectedProced){
        String[] comp_proc = competenze_procedure.visualizeCompetencies(selectedProced);
        jListCompProc.setListData(comp_proc);
        
    }
    
    private void insertAssign(String comp, String proced){
        if(competenze_procedure.insertCompToProced(comp, proced))
            visualizzaCompetenzeProcedure(selectedProced);
        else
            JOptionPane.showMessageDialog(null,"Competence already added for this procedure");
    }
    private void deleteAssign(String competenza){
        
        if(competenze_procedure.delete(competenza))
            visualizzaCompetenzeProcedure(selectedProced);
        else
            JOptionPane.showMessageDialog(null,"ERROR");
    }

