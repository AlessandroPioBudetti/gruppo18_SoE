    private void jRemoveCoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRemoveCoActionPerformed
        String competenza = jListCompProc.getSelectedValue();
        if(competenza == null){
            JOptionPane.showMessageDialog(null,"Oops, have you selected a competencies?","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        deleteAssign(competenza);
    }//GEN-LAST:event_jRemoveCoActionPerformed

    private void jAddCompActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAddCompActionPerformed
        if (selectedComp == null && selectedProced == null) {
            JOptionPane.showMessageDialog(null, "Please select an procedure's identifier and a competence.");
            return;
        }
        if (selectedComp == null) {
            JOptionPane.showMessageDialog(null,"Please select a competence." );
            return;
        }
        if (selectedProced == null) {
            JOptionPane.showMessageDialog(null,"Please select an procedure's identifier." );
            return;
        }
        insertAssign(selectedComp,selectedProced);
    }//GEN-LAST:event_jAddCompActionPerformed

    private void jListProcedureMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListProcedureMouseClicked
        selectedProced = jListProcedure.getSelectedValue();
        visualizzaCompetenzeProcedure(selectedProced);
        if(selectedProced == null){
            JOptionPane.showMessageDialog(null,"Oops, have you selected a procedure?","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jListProcedureMouseClicked

